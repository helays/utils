func (this LoginInfo) QueryIsManage() func(db *gorm.DB) *gorm.DB {
	return func(db *gorm.DB) *gorm.DB {
		if !this.IsManage {
			db.Where("user_id=?", this.UserId)
		}
		return db
	}
}
